# Opertions supported by python

a = 3
b = 2

x = [1, 2, 3, 4]

# Arthematic operations
print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a // b)
print(a ** b)
print(a % b)

# Bitwise operations
print(a & b)
print(a | b)
print(a ^ b)
print(~a)
print(a >> b)
print(a << b)


# Shorthand operation
c = a
c += 1
print(c)

# Conditional operations
print(a > b)
print(a < b)
print(a >= b)
print(a <= b)
print(a != b)
print(a == b)

# Logical, Identity and Membership operations
print((a > b) and (a < b))
print((a > b) or (a < b))
print(not (a < b))

print(a is b)
print(a is not b)

print(a in x)
print(b not in x)


